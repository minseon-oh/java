package quiz03;

public class Calculater {
	
	int a;
	int b;

	void plus() {
		System.out.println(a + "+" + b + "=" + (a+b) );
	}
	
	void minus() {
		System.out.println(a + "-" + b + "=" + (a-b) );
	}
	
	void multiply() {
		System.out.println(a + "x" + b + "=" + (a*b) );
	}
	
	void division() {
		System.out.println(a + "/" + b + "=" + (a/b) );
	}
	
	

}
